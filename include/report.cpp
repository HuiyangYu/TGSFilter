// 将数字向量转为json array
template <typename T>
string vectorNumToJson(const vector<T> &vec)
{
    stringstream ss;
    ss << "[";
    for (size_t i = 0; i < vec.size(); ++i)
    {
        ss << vec[i];
        if (i < vec.size() - 1)
        {
            ss << ",";
        }
    }
    ss << "]";
    return ss.str();
}

